    private void jButton_cadastrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_cadastrarMouseClicked
        String login_cadastro = jTextField_login.getText();
        String senha_cadastro = jTextField_senha.getText();
        cadastrar.cadastrar(login_cadastro, senha_cadastro);
        jLabel_status_login.setText("Usu√°rio cadastrado com sucesso!");
    }//GEN-LAST:event_jButton_cadastrarMouseClicked

    private void jButton_loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_loginMouseClicked
        cadastrar.setLogin(jTextField_login.getText());
        cadastrar.setSenha(jTextField_senha.getText());
        if(cadastrar.login() == true){
            jLabel_status_login.setText("Login com sucesso!");
        }
        else{
            jLabel_status_login.setText("Realize o cadastro!");
        }
    }//GEN-LAST:event_jButton_loginMouseClicked

